// ─── Supabase Upsert Logic ──────────────────────────────────────────────────

async function upsertType(def: ProfileTypeDef): Promise<void> {
  const url = `${SUPABASE_URL}/rest/v1/profile_type_definitions`;

  const upsertUrl = `${url}?on_conflict=type_key`;

  const response = await fetch(upsertUrl, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "Authorization": `Bearer ${SUPABASE_SERVICE_KEY}`,
      "apikey": SUPABASE_SERVICE_KEY,
      "Prefer": "resolution=merge-duplicates,return=minimal",
    },
    body: JSON.stringify({
      type_key: def.type_key,
      label: def.label,
      category: def.category,
      description: def.description,
      icon: def.icon,
      field_schema: def.field_schema,
      tab_config: def.tab_config,
      default_parent_type: def.default_parent_type,
      is_system: def.is_system,
      sort_order: def.sort_order,
    }),
  });

  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(`Failed to upsert "${def.type_key}" (${response.status}): ${errorText}`);
  }
}

// ─── Main ───────────────────────────────────────────────────────────────────

async function main() {
  console.log(`\n🌱 Seeding ${typeDefinitions.length} profile type definitions...\n`);

  const categories = [...new Set(typeDefinitions.map((t) => t.category))];
  let successCount = 0;
  let failCount = 0;

  for (const category of categories) {
    const inCategory = typeDefinitions.filter((t) => t.category === category);
    console.log(`  📁 ${category.toUpperCase()} (${inCategory.length} types)`);

    for (const def of inCategory) {
      try {
        await upsertType(def);
        console.log(`     ✅ ${def.type_key} — ${def.label}`);
        successCount++;
      } catch (err) {
        console.error(`     ❌ ${def.type_key}: ${(err as Error).message}`);
        failCount++;
      }
    }

    console.log();
  }

  console.log("─".repeat(50));
  console.log(`✅ ${successCount} succeeded  |  ❌ ${failCount} failed  |  📦 ${typeDefinitions.length} total`);

  if (failCount > 0) {
    process.exit(1);
  }
}

main().catch((err) => {
  console.error("Fatal error:", err);
  process.exit(1);
});
